-- ════════════════════════════════════════════════════════════
--  Личный портал — сквозная схема «app»
--  PostgreSQL 18 · схема app (общие, межмодульные данные)
-- ════════════════════════════════════════════════════════════
--
--  В отличие от модульных схем (todo, finance, garage, notes),
--  схема app хранит сквозные вещи, не привязанные к одной вкладке:
--  настройки портала, параметры уведомлений Telegram и т.п.
--
--  settings — простое key-value хранилище. Значения всегда text;
--  типизация (число, флаг) — на стороне приложения. Эндпоинт
--  /api/settings отдаёт это как плоский объект { key: value }.
--
--  Известные ключи (фронт «Настройки»):
--    telegram_bot_token     — токен бота портала (секрет!)
--    telegram_chat_id       — куда слать алерты
--    telegram_thread_id     — id топика супергруппы (опционально)
--    telegram_owner_id      — Telegram user id владельца (доступ к Mini App)
--    telegram_webhook_secret— секрет вебхука (генерится автоматически)
--    portal_public_url      — публичный адрес портала (опц., для кнопки/вебхука)
--    birthday_reminder_days — за сколько дней напоминать о ДР (число, по умолч. 7)
--    timezone               — часовой пояс, напр. Europe/Moscow
--
--  Идемпотентно: накатывается при каждом старте бэкенда.
-- ════════════════════════════════════════════════════════════

CREATE SCHEMA IF NOT EXISTS app;

-- ── Настройки портала (key-value) ─────────────────────────────
-- JS: settings = { key: value, ... }
CREATE TABLE IF NOT EXISTS app.settings (
    key        text        PRIMARY KEY,
    value      text,
    updated_at timestamptz NOT NULL DEFAULT now()
);

-- ════════════════════════════════════════════════════════════
--  Права: всё принадлежит пользователю portal
-- ════════════════════════════════════════════════════════════
ALTER SCHEMA app           OWNER TO portal;
ALTER TABLE  app.settings  OWNER TO portal;

-- ════════════════════════════════════════════════════════════
--  Заметки по интеграции
-- ════════════════════════════════════════════════════════════
-- 1. Значения хранятся строками. birthday_reminder_days парсится в
--    число на фронте/в нотификаторе; пустая строка = «не задано».
-- 2. POST /api/settings делает UPSERT одной пары (key, value) и
--    обновляет updated_at.
-- 3. ВАЖНО (ops): /api/settings содержит секрет (токен бота) и
--    предназначен только для внутренней сети. НЕ выставлять наружу
--    через Tailscale Funnel — портал доступен только изнутри tailnet.
